     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
      $new = new Noticia($request->all());
      $new->user_id = \Auth::user()->id; //Obtenemos el user_id del usuario que inicio la sesion
      $new->save();

      flash("Se ha publicado la noticia de manera exitosa!",'success');
      return redirect()->route('news.index');


    }

